package DemoPack;

public class DemoOperators {
    public static void main(String[] args) {
        int a = 9;
        a++;
        System.out.println (a);
         a--;
        System.out.println (a);//9
         ++a;
        System.out.println (a);//10
        System.out.println (a++);//10
        System.out.println (a--);//11
        System.out.println (--a);//9
        System.out.println (++a);//10



    }
}
